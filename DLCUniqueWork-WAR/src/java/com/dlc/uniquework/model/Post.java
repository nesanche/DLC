package com.dlc.uniquework.model;

/**
 *
 * @author fasaloni
 */
public class Post {
    private int id;
    private int count;

    public Post(int id, int count) {
        this.id = id;
        this.count = count;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }    
}
